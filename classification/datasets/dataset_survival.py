class Generic_MIL_Survival_Dataset(Generic_WSI_Survival_Dataset):
	def __init__(self,
		data_dir, 
		**kwargs):
	
		super(Generic_MIL_Survival_Dataset, self).__init__(**kwargs)
		self.data_dir = data_dir
		self.use_h5 = False

	def load_from_h5(self, toggle):
		self.use_h5 = toggle

	def __getitem__(self, idx):
		slide_id = self.slide_data['slide_id'][idx]
		label = self.slide_data['label'][idx]
		censorship = self.slide_data['censorship'][idx]
		event_time = self.slide_data['survival_months'][idx]
		# print(censorship,event_time)
		if type(self.data_dir) == dict:
			source = self.slide_data['source'][idx]
			data_dir = self.data_dir[source]
		else:
			data_dir = self.data_dir

		if not self.use_h5:
			if self.data_dir:
				# print(slide_id)
				full_path = glob.glob(os.path.join(data_dir, 'pt_files', '{}*.pt'.format(slide_id)))[0]
				features = torch.load(full_path)
				return features, label, censorship, event_time
			
			else:
				return features, label, censorship, event_time

		else:
			full_path = os.path.join(data_dir,'h5_files','{}.h5'.format(slide_id))
			with h5py.File(full_path,'r') as hdf5_file:
				features = hdf5_file['features'][:]
				coords = hdf5_file['coords'][:]

			features = torch.from_numpy(features)
			return features, label, coords


